public class Forquilla {
    private int num ;
    private Boolean enUs;
    public Forquilla(int num) {
        this.num = num;
        this.enUs = false;
    }
    
    public Boolean getEnUs() {
        return enUs;
    }
    public void setEnUs(Boolean enUs) {
        this.enUs = enUs;
        
    }
    public int getNum() {
        return num;
    }
    
}
 